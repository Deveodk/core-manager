<?php

namespace DeveoDK\Core\Manager\Resources;

class Relation
{
    /** @var array */
    protected $data;

    /**
     * Relation constructor.
     * @param array $data
     */
    public function __construct(array $data)
    {
        $this->data = $data;
    }

    /**
     * @return array
     */
    public function getData(): array
    {
        return $this->data;
    }

    /**
     * @param array $data
     */
    public function setData(array $data)
    {
        $this->data = $data;
    }
}
